package com.kodilla.stream.reference;

public class FunctionalCalculator {

    public static double sumAToB (double a, double b) {
        return a + b;
    }

    public static double subAFromB (double a, double b) {
        return a - b;
    }

    public static double multiplyAFromB (double a, double b) {
        return a * b;
    }

    public static double divideAByB (double a, double b) {
        return a / b;
    }
}
